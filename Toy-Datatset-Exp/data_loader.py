import random
class StateMachine:
    def __init__(self, init_state=None, state_shift_ratio=0.9, pattern=1):
        self.pattern = pattern
        self.state_shift_ratio = state_shift_ratio
        self.state_list = {1: [1, -1, 0.02, 2],2: [0.5, 1, -0.5, 1.5, -1.5], 3: [1, -2, 1.5, -1, 0.5], 4: [0, 0.5, 1, 1.5, 2, 2.5, 3, 3.5]}
        if init_state is not None:
            self.state = init_state
        else:
            if pattern == 1:
                self.state = 1 if random.random() > self.state_shift_ratio else random.choice(self.state_list[1])
            elif pattern == 2:
                self.state = random.choice(self.state_list[2])
            elif pattern == 3:
                self.state = random.choice(self.state_list[3])
            elif pattern == 4:
                self.state = init_state

    def next_state(self, control=None):
        if self.pattern == 1:
            # 1->0->-1->2->1
            if self.state == 1:
                self.state = 1 if random.random() > self.state_shift_ratio else -1
            elif self.state == -1:
                self.state = 0.02
            elif self.state == 0.02:
            #     self.state = 0.001
            # elif self.state == 0.001:
                self.state = 2
            elif self.state == 2:
                self.state = 1.02
            elif self.state == 1.02:
                self.state = 1
        elif self.pattern == 2:
            # 0.5->1->-0.5->1.5->-1.5->0.5
            if self.state == 0.5:
                self.state = -1# if random.random() > self.state_shift_ratio else -0.5
            elif self.state == -1:
                self.state = -0.5
            elif self.state == -0.5:
                self.state = 1.5
            elif self.state == 1.5:
                self.state = 0
            elif self.state == 0:
                self.state = -1.5
            elif self.state == -1.5:
                self.state = 0.5
        elif self.pattern == 3:
            if control>=0:
                # 1->-2->1.5->-1->0.5->1
                if self.state == 1:
                    self.state = -2
                elif self.state == -2:
                    self.state = 1.5
                elif self.state == 1.5:
                    self.state = -1
                elif self.state == -1:
                    self.state = 0.5
                elif self.state == 0.5:
                    self.state = 1
            elif control<0:
                # 与大于零的情况反向
                # 1->0.5->-1->1.5->-2->1
                if self.state == 1:
                    self.state = 0.5
                elif self.state == 0.5:
                    self.state = -1
                elif self.state == -1:
                    self.state = 1.5
                elif self.state == 1.5:
                    self.state = -2
                elif self.state == -2:
                    self.state = 1
        elif self.pattern == 4:
            # TODO:可以取整
            control = int(control) / 2
            self.state = control
        return self.state

